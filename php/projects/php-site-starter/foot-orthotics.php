<p>Custom made foot orthotics can be constructed specifically to correct your foot mechanics. Orthotics support the arch and relieve foot pain while properly aligning your body from the feet up. 

<h3>How do you make the orthotics?</h3>
<p>The chiropractors at Cordasco Chiropractic will use a computerized gait scan and assessment to diagnose foot problems. A non-weight bearing foot impression using serufoam is the technique used.  A Plaster casting technique is also an option (please call ahead and request this method if this is your preference).The orthotics are manufactured at Atlas Orthotic Lab. To check the complete catalogue for orthopaedic shoes/sandals please visit <a target="_blank" href="http://www.atlasorthoticlab.com">www.atlasorthoticlab.com</a>. 

<h3>Why are orthotics so expensive?</h3>
<p>Orthotics are custom made for your feet and accommodations are made to address any problems you have. The castings are analyzed by a computer technician and made accordingly.

<h3>Will orthotics help my lower back pain?</h3>
<p>Yes, orthotics may help. If your arches have dropped, or the biomechanics of your feet are altered, it can have a direct effect on the weight distribution on the ankle, knee and hip joints resulting in low back pain. 

<h3>How long will a pair last?</h3>
<p>It depends on how often you wear your orthotics.  They should be worn every day, however if you have multiple pairs, they can last anywhere from 2-5 years. 

<h3>Why should I wear orthotics?</h3>
<p>People should wear orthotics for various reasons including: flat feet / fallen arches, heel spurs, plantar fascitis, pronation (over rotation of the foot inwards) and supination (walking on the outside of the foot).

<h3>Will OHIP cover orthotics?</h3>
<p>No. Extended health care benefits may cover the cost of the orthotics. Each insurance company's terms are different. 

<h3>What should I ask my insurance company regarding orthotic coverage?</h3>
